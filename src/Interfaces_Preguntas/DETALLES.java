    private void cargarDetalles() {
        List<CONEX.Pregunta> preguntas = conexion.obtenerPreguntasYOpcionesPorTestId(testId);
        StringBuilder detalles = new StringBuilder();
        for (CONEX.Pregunta pregunta : preguntas) {
            detalles.append(pregunta.getTexto()).append("\n");
            for (CONEX.Opcion opcion : pregunta.getOpciones()) {
                detalles.append("  - ").append(opcion.getTexto());
                if (opcion.esCorrecta()) {
                    detalles.append(" (Correcta)");
                }
                detalles.append("\n");
            }
            detalles.append("\n");
        }
        jTextArea1.setText(detalles.toString());
    }

